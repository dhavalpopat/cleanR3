<h1><b>RECYCLE</b></h1>
<h3 align="justify">
Find out more about recycling and how you can be involved in your local area.
Recycling doesn't only help the environment: we can all benefit from recycling things from around the house, at work and during our leisure time.
</h3>
<img class="image" src="img/recycle.png" />
<br>
<br>
<div id="why-recycle">
<h2><b>Why recycle?</b></h2>
<h3 align="justify">
If you're not already recycling, we explain how easy it is and how you can really make a difference.
For those who already recycle, discover the positive effect your recycling efforts are making and find out what else you could do.
When we recycle, used materials are converted into new products, reducing the need to consume natural resources. 
If used materials are not recycled, new products are made by extracting fresh, raw material from the Earth, through mining and forestry.
Recycling helps conserve important raw materials and protects natural habitats for the future.
When we recycle, used materials are converted into new products, reducing the need to consume natural resources. 
Using recycled materials in the manufacturing process uses considerably less energy than that required 
for producing new products from raw materials – even when comparing all associated costs, like transport.
Plus there are extra energy savings because more energy is required to extract, refine, transport and 
process raw materials ready for industry compared with providing industry-ready materials.
Recycling reduces the need for extracting (mining, quarrying and logging), refining and 
processing raw materials all of which create substantial air and water pollution.
As recycling saves energy it also reduces greenhouse gas emissions, which helps to tackle climate change. 
Current UK recycling is estimated to save more than 18 million tonnes of CO2 a year – the equivalent to taking 5 million cars off the road.
</h3>
</div>

<div id="recycle-at-home">
<h2><b>Recycle at home</b></h2>
<h3 align="justify">
Find out what you can recycle. Now that you know what items you can recycle, find a handy place to store them. 
Make sure you encourage everyone in your house to think whether items can be reused or recycled before they're thrown away.
Nine out of ten of us have household recycling collections, so if you don't already have a recycling box or bag provided by your local council, 
it's worth calling them and checking that you haven't been missed out!
Find out from your council what day your recycling will be collected. In some areas recycling is collected every week and in others it's collected fortnightly.
Mark the date on the calendar or request a recycling calendar from your council.
Here are some top tips to make recycling even easier and save you time: <br>
Some things need to be washed before you put them out for recycling, such as glass jars. Use your old washing up water to give them a quick rinse. <br>
Keep your recycling bin next to the main bin so you can take out the rubbish and recycling at the same time. <br>
You could make recycling part of your weekly supermarket trip and "drop when you shop" at supermarket recycling points.
</h3>
</div>

<div id="recycle-at-school">
<h2><b>Recycle at school</b></h2>
<h3 align="justify">
The recycling department at your local council are the best people to contact to find out about recycling services for schools in your area.
Many local authorities offer their own recycling services to schools. Those that don't have their own recycling services should be able to 
put you in touch with organisations who will collect recycling from schools in your area. Contact the recycling department at your local council to find out more.
There are other local and national organisations offering additional recycling services, such as printer cartridge, old mobile phone or aluminium can recycling. 
These may offer money saving opportunities or even generate a small income. 
</h3>
</div>

<div id="recycle-at-work">
<h2><b>Recycle at work</b></h2>
<h3 align="justify">
The first step in identifying what to recycle involves identifying the key materials that are thrown away. 
In simple terms, this might be achieved by looking in refuse bins at different locations around your premises.
Business rates do not cover waste and recycling services. The cost of the service depends on a number of factors, 
including how much recycling you are producing, where your business is located and what materials you want to recycle. 
Waste contractors will sell some of the materials that they collect from you if they're in the right condition, 
however, the revenue from these materials is generally not enough to offset the cost of providing the service.
Before seeking quotes, you should examine the costs that you are currently paying to use as a benchmark. 
It is highly recommended to seek several quotations in order to achieve the best price.
Ideally, you will reduce the capacity (or frequency of collection) of your refuse service when you start recycling. 
This will help to offset the costs of the service and may lead to savings overall. 
As well as the cost of the service, there are a number of things that should be considered when looking for a service provider and entering into a contract with them.
Clear communications are very important to ensure that people use the recycling bins and put the correct items in them. Tell them why you are starting to recycle - 
for example, why it will save money and what the environmental benefits will be.
</h3>
</div>

<div id="more-to-recycle">
<h2><b>What more to do for recycling?</b></h2>
<h3 align="justify">
There are lots of items in the bathroom waiting to be recycled that can make a huge difference. Here's a handy list to remind you: Toothpaste boxes, 
Toilet roll tubes, Plastic shampoo and conditioner bottles, Shower gel containers, Liquid soap bottles, Bleach and bathroom cleaners. <br> <br>
Empty tissue boxes, Old magazines, Empty deodorant aerosols and hairspray are some products you can recycle from your bedroom. <br> <br>
Did you know that it takes seven days for a recycled newspaper to come back as newspaper again? 
You could be reading your favourite newspaper on recycled paper in no time at all! In living room you can often find these items for recycling:
Envelopes, Newspapers, Cardboard packaging from online shopping and air fresheners. <br> <br>
